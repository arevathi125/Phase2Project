package com.caltech.pojo;

public class Batch {
  private int b_id;
  private String b_name;
  private String b_instructor;
  private String b_date;
  private String b_time;
  
public int getB_id() {
	return b_id;
}
public void setB_id(int b_id) {
	this.b_id = b_id;
}
public String getB_name() {
	return b_name;
}
public void setB_name(String b_name) {
	this.b_name = b_name;
}
public String getB_instructor() {
	return b_instructor;
}
public void setB_instructor(String b_instructor) {
	this.b_instructor = b_instructor;
}
public String getB_date() {
	return b_date;
}
public void setB_date(String b_date) {
	this.b_date = b_date;
}
public String getB_time() {
	return b_time;
}
public void setB_time(String b_time) {
	this.b_time = b_time;
}
}
